ok = 0
fail = 0
for fname in os.listdir(raw):
    if not fname.endswith('.md'):
        continue
    fp = os.path.join(raw, fname)
    with open(fp, 'r', encoding='utf-8') as fh:
        content = fh.read()
    if 'clippings' not in content:
        continue
    if fname not in meta_map:
        print(f'SKIP (no meta): {fname}')
        fail += 1
        continue
    meta = meta_map[fname]
    end_idx = content.find('---', 3)
    if end_idx == -1:
        print(f'SKIP (bad fm): {fname}')
        fail += 1
        continue
    end_idx += 3
    rest = content[end_idx:]
    lines = ['---']
    for key, value in meta.items():
        if key == 'tags':
            lines.append('tags:')
            for tag in value:
                lines.append(f'  - {tag}')
        else:
            lines.append(f'{key}: "{value}"')
    lines.append('---')
    new_content = '\n'.join(lines) + rest
    with open(fp, 'w', encoding='utf-8') as f:
        f.write(new_content)
    sz = os.path.getsize(fp)
    if sz < 100:
        print(f'ERROR (small): {fname} ({sz} bytes)')
        fail += 1
    else:
        print(f'OK: {fname} ({sz} bytes)')
        ok += 1
print(f'\nDone: {ok} OK, {fail} failed')
